package model;

public class Menu {

	private String name;
	private String image;
	private String link;
	
	public Menu() {
		
	}
	
	public Menu(String name, String image, String link) {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}
	
	
	
}
